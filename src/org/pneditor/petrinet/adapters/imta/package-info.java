package org.pneditor.petrinet.adapters.imta;

/**
 * This package's responsibilities are the adaptation of the Petri network
 * model, found in `org.pneditor.petrinet.models.hamcyt` to the interface in
 * `org.pneditor.petrinet`.
 * 
 * The latter interface is a set of abstract classes, to subclassed by this
 * adapter.
 * 
 * This package consists of a set of classes subclassing eachother, or the
 * classes of `org.pneditor.petrinet`, and composed of classes from
 * `org.pneditor.petrinet.models.hamcyt`.
 */